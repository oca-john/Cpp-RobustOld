// use `\n` as the line end;
#include <iostream>
using namespace std;
 
int main() {
    cout << "Runoob \n";
    cout << "Google \n";
    cout << "Taobao";
    return 0;
}

// use `endl` as the line end;
#include <iostream>
using namespace std;
 
int main() {
    cout << "Runoob" << endl;
    cout << "Google" << endl;
    cout << "Taobao";
    return 0;
}
